#!/usr/bin/env node
/**
 * generate-font-data.js
 * 
 * Generates a TypeScript module containing the MaterialCommunityIcons font
 * as a base64-encoded data URI. This allows Expo web builds to inject the
 * font without any HTTP fetch (Metro intercepts TTF URL requests in dev mode
 * and returns HTML/JS instead of binary data, causing OTS parsing errors).
 * 
 * Usage: node scripts/generate-font-data.js
 */
const fs = require('fs');
const path = require('path');

// Find the installed font file (handle monorepo pnpm resolution)
const root = path.resolve(__dirname, '../..');
const fontSearchPaths = [
    path.join(__dirname, '../assets/MaterialCommunityIcons.ttf'),
    path.join(root, 'node_modules/@expo/vector-icons/build/vendor/react-native-vector-icons/Fonts/MaterialCommunityIcons.ttf'),
];

let fontPath = null;
for (const candidate of fontSearchPaths) {
    if (fs.existsSync(candidate)) {
        fontPath = candidate;
        break;
    }
}

if (!fontPath) {
    // Search pnpm deep paths
    const pnpmPath = path.join(root, 'node_modules/.pnpm');
    if (fs.existsSync(pnpmPath)) {
        const dirs = fs.readdirSync(pnpmPath);
        for (const dir of dirs) {
            if (dir.startsWith('@expo+vector-icons')) {
                const candidate = path.join(pnpmPath, dir, 'node_modules/@expo/vector-icons/build/vendor/react-native-vector-icons/Fonts/MaterialCommunityIcons.ttf');
                if (fs.existsSync(candidate)) {
                    fontPath = candidate;
                    break;
                }
            }
        }
    }
}

if (!fontPath) {
    console.error('ERROR: MaterialCommunityIcons.ttf not found!');
    process.exit(1);
}

console.log(`Reading font from: ${fontPath}`);
const fontBuffer = fs.readFileSync(fontPath);
const base64 = fontBuffer.toString('base64');
const dataUri = `data:font/ttf;base64,${base64}`;

// Output directory for the generated file
const outputDir = path.join(__dirname, '../lib');
const outputFile = path.join(outputDir, 'material-community-font.ts');

const content = `/**
 * AUTO-GENERATED by scripts/generate-font-data.js
 * DO NOT EDIT MANUALLY.
 *
 * Contains MaterialCommunityIcons font as a base64 data URI so it can be
 * loaded via Font.loadAsync without relying on HTTP fetch (which fails in
 * Expo dev mode because Metro intercepts TTF URLs as JS bundles).
 *
 * Font size: ${Math.round(fontBuffer.byteLength / 1024)} KB
 * Generated: ${new Date().toISOString()}
 */

// The font-family name used internally by @expo/vector-icons MaterialCommunityIcons
export const MATERIAL_COMMUNITY_FONT_FAMILY = 'material-community';

// Data URI allows Font.loadAsync to receive the binary directly, no network request needed
export const MATERIAL_COMMUNITY_FONT_DATA_URI = '${dataUri}' as const;
`;

fs.writeFileSync(outputFile, content, 'utf8');
console.log(`Generated: ${outputFile}`);
console.log(`Font data URI size: ${Math.round(dataUri.length / 1024)} KB (base64 encoded)`);
